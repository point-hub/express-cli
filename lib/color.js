import { red, green, blue, gray, yellow, bgRed, bgGreen, bgBlue, bgYellow } from "colorette";
export class Color {
    static red(text) {
        return red(text);
    }
    static green(text) {
        return green(text);
    }
    static blue(text) {
        return blue(text);
    }
    static gray(text) {
        return gray(text);
    }
    static yellow(text) {
        return yellow(text);
    }
    static bgRed(text) {
        return bgRed(text);
    }
    static bgGreen(text) {
        return bgGreen(text);
    }
    static bgBlue(text) {
        return bgBlue(text);
    }
    static bgYellow(text) {
        return bgYellow(text);
    }
}
